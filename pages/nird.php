<h2>Qu’est-ce que la démarche NIRD ?</h2>

<p>
NIRD signifie <strong>Numérique Inclusif, Responsable et Durable</strong>.
Cette démarche consiste à repenser les usages numériques pour qu’ils soient :
</p>

<ul>
    <li><strong>Inclusifs</strong> : accessibles à tous</li>
    <li><strong>Responsables</strong> : maîtrisés, éthiques et utiles</li>
    <li><strong>Durables</strong> : réduisant leur impact environnemental</li>
</ul>

<h3>Pourquoi adopter NIRD ?</h3>
<p>
Parce que le numérique représente aujourd’hui :
</p>

<ul>
    <li>4% des émissions de CO₂ mondiales</li>
    <li>Une fracture numérique grandissante entre les publics</li>
    <li>Une dépendance croissante aux services numériques</li>
</ul>

<p>
La démarche NIRD est une transition progressive, accessible à tout établissement.
</p>

