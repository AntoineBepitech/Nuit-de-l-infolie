<h2>Entrer dans la démarche NIRD : un parcours progressif</h2>

<p>
L’entrée dans la démarche NIRD pour un établissement se fait de manière progressive,
afin de permettre aux élèves comme aux équipes éducatives de découvrir les services
 mis en place sans pour autant créer un trop gros choc au sein de l’établissement:
</p>

<p><strong>Première étape: la mobilisation</strong></p>

<p>Cette première partie vise à sensibiliser l’établissement scolaire aux démarches NIRD. </p>
<ul>
   <li>Après l’auto-désignation d’un enseignant volontaire pour devenir le contact NIRD
       de l’établissement, l’équipe éducative de l’établissement est informée des enjeux
       de NIRD.</li>
   <li>Par la suite, l’établissement est mis en contact avec d’autres établissements engagés
       via Tchap afin de partager des expériences et informations pratiques, participation à
       des webinaires organisés par le collectif.</li>
   <li>De premiers supports pédagogiques/techniques sont par la suite partagés dans l’établissement, et la direction de l’établissement ainsi que la collectivité de rattachement (commune, département, région) sont informés quant aux initiatives et aux préparations de futurs échanges.</li>
   <li>Cette mobilisation a avant tout pour but de provoquer une prise de conscience collective, et permettre ainsi d’initier des démarches.</li>
</ul>
<p><strong>Seconde étape: expérimentation</strong></p>

<p>Cette seconde partie vise cette fois à tester des solutions et leur pertinence pour l’établissement ciblé.</p>
<ul>
   <li>Des postes de travail équipés d’un système Linux sont mis en place dans les collèges et lycées, ainsi qu’un club informatique où les élèves participent au reconditionnement d’ordinateurs sous Linux .</li>
   <li>Système PrimTux donné pour des écoles primaires, plus simple à prendre en main et adapté aux usages pédagogiques du premier degré.</li>
   <li>Séances d’appropriation/formation pour les élèves et les enseignants mises en place, mise en place d’un suivi et passage du contact NIRD au stade de coordinateur NIRD, devient chargé d’accompagner ses collègues et centraliser les retours.</li>
   <li>Suivi régulier par la direction pour mesurer l'impact de l’expérimentation, échanges avec la collectivité quant aux aspects logistiques.</li>
   <li>Ces expérimentations sont une manière de tester les eaux pour les établissements, se faire ainsi une idée de la suite de la démarche, le tout accompagné d’un suivi et d’une collectivité intégrée dans la réflexion.</li>
</ul>

<p><strong>Troisième étape: intégration</strong></p>

<p>Cette troisième et dernière partie a cette fois pour but d'inscrire durablement la démarche NIRD au sein de l’établissement et lui donner une portée structurelle.</p>
<ul>
   <li>Intégration des postes testés précédemment dans le parc informatique de l’établissement et interconnexion avec la collectivité de rattachement.</li>
   <li>Inscription de la démarche dans le projet d’établissement (validé par la direction et présentée au conseil d’administration), valorisation de l’expérience auprès de la communauté éducative.</li>
   <li>Le coordinateur devient référent NIRD de l’établissement, la direction devient pilote de la démarche et une coopération structurée avec la collectivité est mise en place pour assurer la continuité du service.</li>
   <li>Résultat final: intégration complète de la démarche, connectée à la collectivité et inscrite dans la durée.
</li>
</ul>

